#!/usr/bin/env python3
"""Convert binary asset files into C headers.

Every regular file found in ``--assets-dir`` is turned into a self-contained
header in ``--out-dir`` that exposes the file's bytes as a ``const`` array plus
its length, so firmware can simply ``#include`` it:

    #include "logo_bin.h"
    Serial.write(logo_bin, logo_bin_len);

The C identifier is derived from the asset's path relative to the assets
directory (e.g. ``fonts/big.bin`` -> ``fonts_big_bin``). Generation is
idempotent: a header is only rewritten when its contents would change, which
keeps PlatformIO's incremental builds fast.

Usage:
    python3 tools/bin2header.py --assets-dir assets --out-dir include/generated
"""

from __future__ import annotations

import argparse
import re
import sys
from pathlib import Path

BYTES_PER_LINE = 12


def sanitize_identifier(name: str) -> str:
    """Turn an arbitrary relative path into a valid C identifier."""
    ident = re.sub(r"[^0-9a-zA-Z]+", "_", name).strip("_")
    if not ident:
        ident = "asset"
    if ident[0].isdigit():
        ident = "_" + ident
    return ident.lower()


def guard_macro(identifier: str) -> str:
    return f"ASSET_{identifier.upper()}_H"


def render_header(identifier: str, source_name: str, data: bytes) -> str:
    guard = guard_macro(identifier)
    lines = [
        "/* Auto-generated by tools/bin2header.py. Do not edit by hand. */",
        f"/* Source asset: {source_name} ({len(data)} bytes) */",
        f"#ifndef {guard}",
        f"#define {guard}",
        "",
        "#include <stddef.h>",
        "#include <stdint.h>",
        "",
        f"static const unsigned char {identifier}[] = {{",
    ]

    for offset in range(0, len(data), BYTES_PER_LINE):
        chunk = data[offset : offset + BYTES_PER_LINE]
        hex_bytes = ", ".join(f"0x{b:02x}" for b in chunk)
        lines.append(f"    {hex_bytes},")

    lines.extend(
        [
            "};",
            "",
            f"static const size_t {identifier}_len = {len(data)};",
            "",
            f"#endif /* {guard} */",
            "",
        ]
    )
    return "\n".join(lines)


def iter_assets(assets_dir: Path):
    """Yield (relative_path, absolute_path) for every regular asset file."""
    for path in sorted(assets_dir.rglob("*")):
        if not path.is_file():
            continue
        # Skip dotfiles / placeholders like .gitkeep.
        if any(part.startswith(".") for part in path.relative_to(assets_dir).parts):
            continue
        yield path.relative_to(assets_dir), path


def write_if_changed(target: Path, contents: str) -> bool:
    """Write ``contents`` to ``target`` only if it differs. Returns True if written."""
    if target.exists() and target.read_text(encoding="utf-8") == contents:
        return False
    target.parent.mkdir(parents=True, exist_ok=True)
    target.write_text(contents, encoding="utf-8")
    return True


def generate(assets_dir: Path, out_dir: Path) -> int:
    if not assets_dir.is_dir():
        print(f"bin2header: assets dir not found: {assets_dir}", file=sys.stderr)
        return 1

    out_dir.mkdir(parents=True, exist_ok=True)
    generated = 0
    seen_idents: dict[str, str] = {}

    for rel_path, abs_path in iter_assets(assets_dir):
        # The identifier is derived from the asset's full relative path,
        # extension included, so `banner.bin` -> `banner_bin` and
        # `fonts/big.bin` -> `fonts_big_bin`. The matching header is the
        # identifier + ".h" (e.g. `banner_bin.h`).
        identifier = sanitize_identifier(str(rel_path))
        if identifier in seen_idents:
            print(
                f"bin2header: identifier collision '{identifier}' between "
                f"{seen_idents[identifier]} and {rel_path}",
                file=sys.stderr,
            )
            return 1
        seen_idents[identifier] = str(rel_path)

        data = abs_path.read_bytes()
        header_name = identifier + ".h"
        target = out_dir / header_name
        contents = render_header(identifier, str(rel_path), data)
        changed = write_if_changed(target, contents)
        status = "wrote" if changed else "up-to-date"
        print(f"bin2header: {status} {target} ({len(data)} bytes)")
        generated += 1

    if generated == 0:
        print(f"bin2header: no assets found in {assets_dir} (nothing to do)")

    return 0


def parse_args(argv=None) -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--assets-dir",
        default="assets",
        type=Path,
        help="Directory containing binary assets (default: assets)",
    )
    parser.add_argument(
        "--out-dir",
        default="include/generated",
        type=Path,
        help="Directory to write generated headers (default: include/generated)",
    )
    return parser.parse_args(argv)


def main(argv=None) -> int:
    args = parse_args(argv)
    return generate(args.assets_dir, args.out_dir)


if __name__ == "__main__":
    raise SystemExit(main())
